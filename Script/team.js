const showSocial1 = (toggleCard1, socialCard1) =>{
   const toggle = document.getElementById(toggleCard1),
         social= document.getElementById(socialCard1)

    toggle.addEventListener('click' , () => {
       if(social.classList.contains('animation')){
        social.classList.add('down-animation')
       }

       setTimeout( () => {
        social.classList.remove('down-animation')
       }, 1000) 

        social.classList.toggle('animation')
    })     
}
showSocial1('card-toggle1', 'card-social1')

